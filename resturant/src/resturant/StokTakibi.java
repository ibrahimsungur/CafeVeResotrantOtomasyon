    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
       if(!jTextField2.getText().isEmpty())
        {
            if(jTable1.getRowCount()==0)
            {
            JOptionPane.showMessageDialog(null, "Cüncellenecek Veri Bulunamadı!!!."); 
            }
            if(jTable1.getRowCount()>0 && jTable1.getSelectedRow()!=-1)
            {
            jTable1.setModel(model);
       
           d=model.getValueAt(jTable1.getSelectedRow(),0).toString();
           // System.out.println(d);
           db.urunGuncele(jTextField2.getText(),d);
      
         urunlerListele();
          jTextField2.setText("");
            }
            else
            {
             JOptionPane.showMessageDialog(null, "Cüncellenecek Veri Seçilemedi!!!."); 
            }
        }
       else
       {
        JOptionPane.showMessageDialog(null, "Lütfen Değiştireceğiniz Veriyi Giriniz.. !!!."); 
       }
    }//GEN-LAST:event_jButton4ActionPerformed

